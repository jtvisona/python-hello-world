# Demonstrate basic string functionality
temp_string = "   This is a string: TESTING.   "

print( temp_string )            # As is
print( temp_string.upper() )    # All to uppercase
print( temp_string.lower() )    # All to lowercase
print ( temp_string.left() )    # Remove preceding whitespace
print ( temp_string.right() )   # Remove trailing whitespace
print( temp_string.strip() )    # Remove preceding and trailing whitespace
